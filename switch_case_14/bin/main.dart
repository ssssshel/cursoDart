void main(List<String> args) {
  int opcion;
  opcion = 5;

  switch (opcion) {
    case 1:
      print('Estás en la opción 1');
      break;
    case 2:
      print('Estás en la opción 2');
      break;
    case 3:
      print('Estás en la opción 3');
      break;
    default:
      print("No estás en ninguna opción");
      break;
  }
}
